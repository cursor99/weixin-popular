package weixin.popular.bean.card;

public class NamedUrl {
	private String name;
	private String url;
	public String getName() {
		return name;
	}
	public NamedUrl setName(String name) {
		this.name = name;
		return this;
	}
	public String getUrl() {
		return url;
	}
	public NamedUrl setUrl(String url) {
		this.url = url;
		return this;
	}
	
}
